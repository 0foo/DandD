<p>Welcome to the Wiki dedicated to the <a href="http://www.eaae.be">EAAE/AEEA</a> Charter on Architectural Research.</p>
<p>The Charter is intended as a reference document for the use of universities, architecture schools, research institutions, funding agencies, and professional practices that are undertaking architectural research. It specifies the character and objectives of architectural research, confirms the variety of valid methodologies and supports the development of a vibrant, internationally recognized and well funded research community.</p>
<p>The Charter consists of two parts, a one-page &quot;Declaration&quot; and a more detailed &quot;Framework&quot;.<br />
The &quot;Declaration on Architectural Research&quot; was approved by the EAAE General Assembly in Chania, September 2011; the &quot;Framework for Architectural Research&quot; was accepted as a clarifying text, intended to develop and extend the principles of the Declaration.</p>
<p>The intention of this wiki is to allow the EAAE/AEEA community for further debate on architectural research and contribution to the content of the Framework text. Therefore each paragraph of the text is provided with a comment section which will be open until May 15th, 2012. Subsequently the EAAE research subgroup will take the comments into consideration for amendment of the Framework text, aiming for an approval of both declaration and framework as a package in Chania, September 2012.</p>
